function detectConceptCategory(routeKey) {
  if (routeKey.includes("ExponentNotation")) return "exponentNotation"
  if (routeKey.includes("AlgebraExpressions")) return "algebraExpressions"
  if (routeKey.includes("Factoring")) return "factoring"
  if (routeKey.includes("RationalExpressions")) return "rationalExpressions"
  if (routeKey.includes("EquationsInequalities")) return "equationsInequalities"
  if (routeKey.includes("Systems")) return "systems"
  if (routeKey.includes("SquareRoot")) return "squareRoot"
  if (routeKey.includes("ExpLog")) return "expLog"
  if (routeKey.includes("ParametersFunctions")) return "parametersFunctions"
  if (routeKey.includes("RightTriangleTrig") || routeKey.includes("MetricTrig") || routeKey.includes("TrigAreaProof")) return "trigBasic"
  if (routeKey.includes("SineHeron") || routeKey.includes("SineCosineLaw")) return "trigAdvanced"
  if (routeKey.includes("SimilarityProof") || routeKey.includes("TrianglesSolids")) return "geometryProof"
  if (routeKey.includes("LineEquations") || routeKey.includes("DistanceCorrelation") || routeKey.includes("AnalyticStats")) return "analyticStats"
  if (routeKey.includes("Periodic") || routeKey.includes("StepFunction") || routeKey.includes("Piecewise") || routeKey.includes("QuadraticInteger")) return "specialFunctions"
  if (routeKey.includes("Quadratic")) return "quadratic"
  if (routeKey.includes("Exponential")) return "exponential"
  return "generic"
}

